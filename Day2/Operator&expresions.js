console.log("Operators in js : \n");
let a = 2 , b = 2 ;
console.log("a + b is : ",a+b);
console.log("a - b is : ",a-b);
console.log("a * b is : ",a*b);
console.log("a / b is : ",a/b);
console.log("a % b is : ",a%b);
console.log("a ** b is : ",a**b);
console.log("a++ is : ", (a++) ); //post - increment
console.log(a);
console.log("b-- is : ",(b--));
console.log(b);
console.log("++a is : ", (++a) ); //pre-incement
console.log("--b is : ", (--b) );




